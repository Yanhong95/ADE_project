    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        int row = flightCatalog.getRowCount();
        if(row<1) {
            JOptionPane.showMessageDialog(null, "Please create new filght", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Flight f;
        String flightName = flightNameTextField.getText();
        f = airliner.getFlightDirectory().searchFlight(flightName);
        ViewFlightDetailJPanel vfdjp = new ViewFlightDetailJPanel (userProcessContainer, f);
        userProcessContainer.add("ViewFlightDetailJPanel", vfdjp);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        flightNameTextField.setText("");
    }//GEN-LAST:event_searchBtnActionPerformed

