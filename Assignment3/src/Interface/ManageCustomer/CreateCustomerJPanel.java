    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if(nameTextField.getText().isEmpty()||ageTextField.getText().isEmpty()||idTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty content, Please recreate!");
            return;
        }
        try{
            Integer.parseInt(idTextField.getText());
            Integer.parseInt(ageTextField.getText());
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "The ID and age of customer must be number !  ");
            return;
        }
        Customer customer = customerDirectory.addCustomer();
        customer.setName(nameTextField.getText());
        customer.setAge(Integer.parseInt(ageTextField.getText()));
        customer.setID(Integer.parseInt(idTextField.getText()));
        JOptionPane.showMessageDialog(null, "Customer successfully added", "Warning", JOptionPane.INFORMATION_MESSAGE);
        nameTextField.setText("");
        ageTextField.setText("");
        idTextField.setText("");
    }//GEN-LAST:event_addBtnActionPerformed

