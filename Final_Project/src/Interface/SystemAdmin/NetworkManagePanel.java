    public void showInfo(){
        DefaultTableModel dtm = (DefaultTableModel) tbNetwork.getModel();
        dtm.setRowCount(0);
        for(Network nw : system.getNetworkList()){
            Object row[] = new Object[2];
            row[0] = nw;
            row[1] = nw.getEnterpriseDirectory().getEnterpriseList().size();
            dtm.addRow(row);
        }
        
        cbNet.removeAllItems();
        for(Network n : system.getNetworkList()){
            cbNet.addItem(n);
        }
    }

