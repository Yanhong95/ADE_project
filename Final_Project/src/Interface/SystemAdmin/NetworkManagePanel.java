    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String netName = txtName.getText();
        if(netName==null || netName.equals("")){
            JOptionPane.showMessageDialog(null, "The Name of network can not be empty!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for(Network w : system.getNetworkList()){
            if(netName.equals(w.getName())){
                JOptionPane.showMessageDialog(null, "The network of this area has already exist!", "Alert", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Network nw = new Network();
        nw.setName(netName);
        system.getNetworkList().add(nw);
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Create Network Successfully");
        showInfo();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        // TODO add your handling code here:
        Network net = (Network)cbNet.getSelectedItem();
        double sales = 0;
        if(net.getEnterpriseDirectory().getEnterpriseList().size()>0){
            for(Enterprise en : net.getEnterpriseDirectory().getEnterpriseList()){
                if(en.getEnterpriseType() == Enterprise.EnterpriseType.BookStore){
                    BookStoreEnterprise bs = (BookStoreEnterprise)en;
                    for(Order or : bs.getOrderList()){
                        sales = sales + or.getTotalPrice();
                    }
                }
            }
        }
        txtSales.setText("$"+sales);
    }//GEN-LAST:event_btnCheckActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbNetwork.getSelectedRow();
        Network selectedItem;
        if(selectedRow>=0){
            selectedItem = (Network) tbNetwork.getValueAt(selectedRow, 0);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a network!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        system.getNetworkList().remove(selectedItem);
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Remove Network Successfully");
        showInfo();
    }//GEN-LAST:event_btnRemoveActionPerformed

