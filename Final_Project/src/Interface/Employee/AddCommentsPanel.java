    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = UserCommentList.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        BookComment bookComment = (BookComment)UserCommentList.getValueAt(row, 0);
        Double doubleLike = (double)bookComment.getLike();
        int bookID = bookComment.getBookID();
        
        for (Book book : system.getBookDirectory().getBookList()){
            if(book.getBookID() == bookID){
                int bookCommentSize = book.getBookCommentList().size();
                double bookRating = (calculateBookRating(book)+doubleLike)/(bookCommentSize+1);      
                book.setRating(bookRating);
                book.getBookCommentList().add(bookComment);
                organization.getBookCommentList().remove(bookComment);
                refreshTable();
            }
        }
        JOptionPane.showMessageDialog(null, "Comment add successfully!");
        dB4OUtil.storeSystem(system);
    }//GEN-LAST:event_jButton3ActionPerformed


