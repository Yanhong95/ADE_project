    public void refreshTable(){
        DefaultTableModel model = (DefaultTableModel)UserCommentList.getModel();
        model.setRowCount(0);
        for(BookComment comment : organization.getBookCommentList()){
            Object[] row = new Object[model.getColumnCount()];
            row[0] = comment;
            row[1] = comment.getCommentID();
            row[2] = comment.getContent();
            row[3] = comment.getLike();
            row[4] = comment.getDateCreated();
            model.addRow(row);
        }
    }
    public double calculateBookRating(Book book){
        int like = 0;
        if(book.getBookCommentList().isEmpty()){
            Double doubleLike = (double)like;
            return doubleLike;
        }else{
        for(BookComment bookComment : book.getBookCommentList()){
            like += bookComment.getLike();
        }
            Double doubleLike = (double)like;
            return doubleLike;    
        }
    }

