    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = UserCommentList.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        BookComment bookComment = (BookComment)UserCommentList.getValueAt(row, 0);
        for(int i=0; i<system.getBookDirectory().getBookList().size();i++){
            Book book = system.getBookDirectory().getBookList().get(i);
            for(int j=0; j < book.getBookCommentList().size();j++){
                BookComment bookcommentExample =  book.getBookCommentList().get(j);
                if(bookComment.getCommentID() == bookcommentExample.getCommentID()){
                    if(book.getBookCommentList().size()-1 > 0){
                    Double newRating=(book.getRating()*book.getBookCommentList().size()-bookcommentExample.getLike())/(book.getBookCommentList().size()-1);
                    book.setRating(newRating);
                    }else{
                    book.setRating(0);
                    }
                    book.getBookCommentList().remove(bookcommentExample);                    
            }           
            }
        }
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Comment delete successfully!");
        populate(); 
    }//GEN-LAST:event_jButton3ActionPerformed


