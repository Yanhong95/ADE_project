    public void populate(){
        DefaultTableModel model = (DefaultTableModel) UserCommentList.getModel();
        model.setRowCount(0);
        for(Book book : system.getBookDirectory().getBookList()){
            for(BookComment bookcomment: book.getBookCommentList()){
                Object[] row = new Object[model.getColumnCount()];
                row[0] = bookcomment;
                row[1] = bookcomment.getCommentID();
                row[2] = book;
                row[3] = bookcomment.getContent();
                row[4] = bookcomment.getDateCreated();
                model.addRow(row);
            }
        }
    
    }

