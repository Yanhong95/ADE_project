    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(organization==null){
            JOptionPane.showMessageDialog(null, "Please select an organization!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String uname = txtName.getText();
        if(uname==null || uname.equals("")){
            JOptionPane.showMessageDialog(null, "The Name of employee can not be empty!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        char[] passwordCharArray = txtPwd.getPassword();
        String password = String.valueOf(passwordCharArray);
        if(password==null || password.equals("")){
            JOptionPane.showMessageDialog(null, "The Password of employee can not be empty!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }

        long id = getGenerateID();
        Employee employee = organization.getEmployeeDirectory().createEmployee(id,uname,password);
        employee.setOrg(organization);
        system.getEmployeeDirectory().getEmployeeList().add(employee);
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Create Employee Successfully\nNew Employee ID is "+employee.getId());
        populateTable();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbEmp.getSelectedRow();
        Employee selectedItem;
        if(selectedRow>=0){
            selectedItem = (Employee) tbEmp.getValueAt(selectedRow, 0);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select an employee!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Organization o = (Organization) tbEmp.getValueAt(selectedRow, 2);
        o.getEmployeeDirectory().getEmployeeList().remove(selectedItem);
        system.getEmployeeDirectory().getEmployeeList().remove(selectedItem);
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Remove Employee Successfully");
        populateTable();
    }//GEN-LAST:event_btnRemoveActionPerformed

