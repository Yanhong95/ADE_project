    public void setOrg(){
        cbOrg.removeAllItems();
        cbOrg.addItem(null);
        for(Organization o : admin.getEnterprise().getOrganizationDirectory().getOrganizationList()){
            cbOrg.addItem(o);
        }
    }
    
    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tbEmp.getModel();
        dtm.setRowCount(0);
        if(organization == null){
            for(Organization or : admin.getEnterprise().getOrganizationDirectory().getOrganizationList()){
                for(Employee em : or.getEmployeeDirectory().getEmployeeList()){
                    Object row[] = new Object[3];
                    row[0] = em;
                    row[1] = em.getName();
                    row[2] = or;
                    dtm.addRow(row);
                }
            }
        }
        else{
            for(Employee em : organization.getEmployeeDirectory().getEmployeeList()){
                Object row[] = new Object[3];
                row[0] = em;
                row[1] = em.getName();
                row[2] = organization;
                dtm.addRow(row);
            }
        }
    }

    public static long getGenerateID(){
        int num = 6;    
        String numStr = "";
        for(int i = 0; i < num; i++){
            numStr += (int)(10*(Math.random()));
        }
         
        long id = Integer.parseInt(numStr);
        return id;
     }
