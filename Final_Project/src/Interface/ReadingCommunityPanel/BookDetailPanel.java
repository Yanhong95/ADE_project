    public void showDetail(){
        nameTxt.setText(book.getTitle());
        DecimalFormat df = new DecimalFormat("0.000");
        ratingTxt.setText(String.valueOf(df.format(book.getRating())));
        desArea.setText(book.getDescription());
        authorTxt.setText(book.getAuthor());
        ImageIcon icon = new ImageIcon(book.getPicture());
        icon.setImage(icon.getImage().getScaledInstance(144, 179, SCALE_DEFAULT));
        pictureLab.setIcon(icon);
    }
    public void populate(){
        DefaultTableModel model = (DefaultTableModel) commentTable.getModel();
        model.setRowCount(0);
        for(BookComment bookcomment : book.getBookCommentList()){
            Object[] row = new Object[model.getColumnCount()];
            row[0] = bookcomment;
            row[1] = bookcomment.getLike();
            row[2] = bookcomment.getDateCreated();
            model.addRow(row);
        }
    }


