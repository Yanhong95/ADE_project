    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:CommentDetailPanel
        int row = commentTable.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        BookComment bookcomment = (BookComment)commentTable.getValueAt(row, 0);
        CommentDetailPanel panel = new CommentDetailPanel(system,bookcomment,rightPanel);
        rightPanel.add("CommentDetailPanel", panel);
        CardLayout cl = (CardLayout) rightPanel.getLayout();
        cl.previous(rightPanel);
    }//GEN-LAST:event_viewBtnActionPerformed


