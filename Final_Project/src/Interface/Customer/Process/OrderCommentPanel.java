    private void btnRatingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRatingActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbItem.getSelectedRow();
        OrderItem selectedItem;
        if(selectedRow>=0){
            selectedItem = (OrderItem) tbItem.getValueAt(selectedRow, 0);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select an item!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String strrate = txtRating.getText();
        double rating = 0;
        try{
            rating = Double.parseDouble(strrate);
            if(rating>10 || rating<0){
                JOptionPane.showMessageDialog(null, "Please input a number between 0 and 10!", "Alert", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please input a number between 0 and 10!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        selectedItem.setRating(rating);
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Rating Successfully");
        populateTable();
        txtRating.setText("");
    }//GEN-LAST:event_btnRatingActionPerformed

