    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbItem.getSelectedRow();
        OrderItem selectedItem;
        if(selectedRow>=0){
            selectedItem = (OrderItem) tbItem.getValueAt(selectedRow, 0);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select an item!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ItemComment ic = new ItemComment(user.getUserID(),areaComment.getText(),order.getOrderID(),selectedItem.getOrderItemID());
        selectedItem.setComment(ic);
        selectedItem.getItem().getCommentList().add(ic);
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(null, "Submit Successfully");
    }//GEN-LAST:event_btnSubmitActionPerformed

    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tbItem.getModel();
        dtm.setRowCount(0);
        for(OrderItem oi : order.getOrderItemList()){
            Object row[] = new Object[4];
            row[0] = oi;
            row[1] = oi.getItem().getPrice();
            row[2] = oi.getSaleQuantity();
            row[3] = oi.getRating();
            dtm.addRow(row);
        }
    }

