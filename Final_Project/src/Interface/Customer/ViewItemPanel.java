    public void showInfo(){
        String username = user.getUsername();
        String title = item.getBook().getTitle();
        String author = item.getBook().getAuthor();
        double points = calPoints();
        item.setRating(points);
        double price = item.getPrice();
        String picPath = item.getPicture();
        String des = item.getDescription();
        
        lbWelcome.setText("Welcome,  "+username);
        lbTitle.setText(title);
        lbAuthor.setText("by  "+author);
        lbPoints.setText("Points: "+points);
        lbPrice.setText("$ "+price);
        areaDescription.setText(des);
        areaDescription.setEditable(false);
        ImageIcon icon = new ImageIcon(picPath);
        icon.setImage(icon.getImage().getScaledInstance(144, 179, SCALE_DEFAULT));
        lbPicture.setIcon(icon);
        
        areaDescription.setLineWrap(true);
        areaDescription.setWrapStyleWord(true);
        //rightPanel.add(new JScrollPane(areaDescription));
    }

    public double calPoints(){
        List<Order> list = enterprise.getOrderList();
        System.out.println(list.size());
        double avePoint = 0;
        double totalPoint = 0;
        int num = 0;
        for(Order or : list){
            for(OrderItem oi : or.getOrderItemList()){
                if(item.equals(oi.getItem())){
                    totalPoint = totalPoint + oi.getRating();
                    num++;
                }
            }
        }
        if(num == 0){
            avePoint = 8;
        }
        else{
            avePoint = totalPoint/num;
        }
        return avePoint;
    }
    
