    private void btnCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCartActionPerformed
        // TODO add your handling code here:
        if(item.getQuantity()>0){
            if(customer.getCartList().size()>0){  
                String ma = "d";
                for(OrderItem oi : customer.getCartList()){
                    if(item.equals(oi.getItem())){
                        int num = oi.getSaleQuantity();
                        oi.setSaleQuantity(num+1);
                        ma = "s";
                    }
                }
                if(ma.equals("d")){
                    int len = customer.getCartList().size();
                    OrderItem newoi = new OrderItem(len,item,1);
                    customer.getCartList().add(newoi);
                }
                
            }
            else{
                OrderItem newoi = new OrderItem(0,item,1);
                customer.getCartList().add(newoi);
            }
            dB4OUtil.storeSystem(system);
            JOptionPane.showMessageDialog(null, "Add to Cart Successfully");
        }
        else{
            JOptionPane.showMessageDialog(null, "Sorry, no product stored in inventory.\nYou can try to search in another store", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnCartActionPerformed

