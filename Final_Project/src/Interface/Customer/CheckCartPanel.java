    private void btnPlaceAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceAllActionPerformed
        // TODO add your handling code here:
        ArrayList<OrderItem> list = new ArrayList<>();
        list.addAll(customer.getCartList());
        if(list.size()<1){
            JOptionPane.showMessageDialog(null, "No item in your cart!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        OrderPlacePanel panel = new OrderPlacePanel(system,user,list,rightPanel);
        rightPanel.add("OrderPlacePanel", panel);
        CardLayout cl = (CardLayout) rightPanel.getLayout();
        cl.next(rightPanel);
    }//GEN-LAST:event_btnPlaceAllActionPerformed

    private void btnPlaceOneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOneActionPerformed
        // TODO add your handling code here:
        ArrayList<OrderItem> list = new ArrayList<>();
        int selectedRow = tbCart.getSelectedRow();
        OrderItem selectedItem;
        if(selectedRow>=0){
            selectedItem = (OrderItem) tbCart.getValueAt(selectedRow, 0);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select an item!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        list.add(selectedItem);
        OrderPlacePanel panel = new OrderPlacePanel(system,user,list,rightPanel);
        rightPanel.add("OrderPlacePanel", panel);
        CardLayout cl = (CardLayout) rightPanel.getLayout();
        cl.next(rightPanel);
    }//GEN-LAST:event_btnPlaceOneActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbCart.getSelectedRow();
        OrderItem selectedItem;
        if(selectedRow>=0){
            selectedItem = (OrderItem) tbCart.getValueAt(selectedRow, 0);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select an item!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
        BookStoreEnterprise enterprise = (BookStoreEnterprise)selectedItem.getItem().getEnterprise();
        ViewItemPanel panel = new ViewItemPanel(system,enterprise,user,selectedItem.getItem(),"source cart",rightPanel);
        rightPanel.add("ViewItemPanel", panel);
        CardLayout cl = (CardLayout) rightPanel.getLayout();
        cl.next(rightPanel);
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbCart.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to remove?","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                OrderItem selitem = (OrderItem)tbCart.getValueAt(selectedRow, 0);
                customer.getCartList().remove(selitem);
                dB4OUtil.storeSystem(system);
                populate();
            }             
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select an item!", "Alert", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel) tbCart.getModel();
        dtm.setRowCount(0);
        for(OrderItem m : customer.getCartList()){
            Object row[] = new Object[4];
            row[0] = m;
            row[1] = m.getItem().getBook().getAuthor();
            row[2] = m.getItem().getPrice();
            row[3] = m.getSaleQuantity();
            dtm.addRow(row);
        }
    }

