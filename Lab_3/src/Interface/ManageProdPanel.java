    private void btnViewProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewProductActionPerformed
        // TODO add your handling code here:
        int selectedRow= tblDirectory.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to view details!","Warning", JOptionPane.WARNING_MESSAGE); 
        }else{
            Product prod=(Product)tblDirectory.getValueAt(selectedRow,0);
            ViewPanel vPanel= new ViewPanel(rightPanel,prod,prodDir);
            rightPanel.add("ViewPanel",vPanel);
            CardLayout layout = (CardLayout)rightPanel.getLayout();
            layout.next(rightPanel);
        }
       
        
    }//GEN-LAST:event_btnViewProductActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        Product result=prodDir.searchProduct(txtsearch.getText());
        if (result == null){
            JOptionPane.showMessageDialog(null,"Product name you enterd does not exist! ","Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            ViewPanel vPanel= new ViewPanel(rightPanel,result,prodDir);
            rightPanel.add("ViewPanel",vPanel);
            CardLayout layout = (CardLayout)rightPanel.getLayout();
            layout.next(rightPanel);
        }

    }//GEN-LAST:event_btnSearchActionPerformed

